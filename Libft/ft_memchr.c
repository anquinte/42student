#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ps;

	ps = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (*ps == (unsigned char)c)
			return ((void *)ps);
		ps++;
		i++;
	}
	return (NULL);
}
